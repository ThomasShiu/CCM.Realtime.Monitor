using CCM.Application;
using CCM.Application.SystemSecurity;
using CCM.Code;
using CCM.Domain.Entity;
using CCM.Domain.Entity.SystemSecurity;
using System.Web.Mvc;

//todo: 請修改對應的namespace
namespace CCM.RTM.Admin.Areas.SystemSecurity.Controllers
{
    public class ActionLogController : ControllerBase
    {
        private ActionLogApp tableApp = new ActionLogApp();

        [HttpGet]
        public ActionResult RemoveLog()
        {
            return View();
        }

        [HttpGet]
        [HandlerAjaxOnly]
        public ActionResult GetGridJson(Pagination pagination, string queryJson)
        {
            var data = new
            {
                rows = tableApp.GetList(pagination, queryJson),
                total = pagination.total,
                page = pagination.page,
                records = pagination.records
            };
            return Content(data.ToJson());
        }

        public ActionResult GetGridJson(string keyword)
        {
            var data = tableApp.GetList(keyword);
            return Content(data.ToJson());
        }

        [HttpGet]
        [HandlerAjaxOnly]
        public ActionResult GetFormJson(string keyValue)
        {
            var data = tableApp.GetForm(keyValue);
            return Content(data.ToJson());
        }
        [HttpPost]
        [HandlerAjaxOnly]
        [ValidateAntiForgeryToken]
        public ActionResult SubmitForm(ActionLogEntity entity, string keyValue)
        {
            tableApp.SubmitForm(entity, keyValue);
            return Success("操作成功。");
        }
        [HttpPost]
        [HandlerAjaxOnly]
        [HandlerAuthorize]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteForm(string keyValue)
        {
            tableApp.DeleteForm(keyValue);
            return Success("删除成功。");
        }

        [HttpPost]
        [HandlerAjaxOnly]
        [HandlerAuthorize]
        [ValidateAntiForgeryToken]
        public ActionResult SubmitRemoveLog(string keepTime)
        {
            tableApp.RemoveLog(keepTime);
            return Success("清空成功。");
        }
    }


}
